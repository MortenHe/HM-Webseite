                    <h1>Hilfskraft für die Juniordetektive</h1>
                    <p class='absatz'>Eine Hauptrolle bekam darüber hinaus stets <span class='kursiv'>Carlos</span> ("Si, si Senior Jonas..."), den Justus und Co. als willige Hilfskraft und Sündenbock benutzten ("Das ist alles wieder deine Schuld, Carlos") und der eigentlich nie ausreden durfte, obwohl er doch schlauer war als das Detektivtrio. Einen kleinen Gastauftritt erhielt, wenn möglich, auch sein Onkel Ramos.</p>
                    <p>Justus war natürlich auch bei uns unantastbar als <span class='kursiv'>Erster</span> Detektiv: Er hat im Lauf der Hörspiele eine kleine Wandlung vollzogen: Im ersten Hörspiel (in dem es generell noch ernster zuging) kam Justus nämlich dem Original sehr nahe. Er war etwas altklug, dafür aber freundlich und lag mit seinen Schlüssen richtig. Das änderte sich in den nächsten beiden Hörspielen, wo <span class='kursiv'>Justus Jonas Junior</span> ein ziemliches Ekel abgab.</p>
