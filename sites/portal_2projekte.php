                    <h1>Justus Jonas Junior</h1>
                    <p class='absatz'>Auf die Liste der Verdächtigen setzte Justus nun mitunter auch Peter und Bob, die in Nebenrollen als blasse Gehilfen auftraten. Wenig Hilfsreiches, dafür aber viele Befehle und Belehrungen gingen während der Lösung der letzten Fälle auf Justus' Konto. Gelegenheiten, sich aufzuspielen, nutzte er sehr gerne, besonders als im dritten Hörspiel ein schönes Mädchen um Hilfe bat. Das leidige Thema Übergewicht rückte allerdings auch wieder stärker in den Vordergrund.</p>
