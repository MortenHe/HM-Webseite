                    <h1>Schrottplatz und Pizzeria</h1>
                    <p class='absatz'>Hörspielserien über Kinder und Jugendliche, die in ihrer Freizeit Detektiv spielen, sind ja bereits Klassiker geworden. Es ist eigentlich egal, ob die Nachwuchsdetektive ihr Hauptquartier auf einem Schrottplatz oder in einer Pizzeria einrichten, ob sie einen Kriminalkommissar oder bekannten Wissenschaftler in der Verwandtschaft haben und ob sie ein Tier als Mitglied akzeptieren oder nicht.</p>
                    <p>Die Helden dieser Hörspielwelt erforschen unentwegt Geheimnisse, lösen Rätsel und meistern gefährliche Situationen, mit ihren Erlebnissen kann unser Alltag kaum mithalten. Dazu vermarkten sie ihre Abenteuer auch geschickt und lassen ihre Geschichten regelmäßig auf Kassette und CD erscheinen.</p>
                    <p>Bleibt die Möglichkeit, sich mit diesen Hörspielklassikern selbst schöpferisch auseinanderzusetzen, wie es in vielen <span class='kursiv'>Fanhörspielen</span> gerne mit Parodien gemacht wird. Wegen der Klischees und bestimmter Konventionen sind diese Hörspiele eigentlich auch ohne eigenes Zutun schon witzig, obgleich man das als Kind nicht so wahrnimmt. Nachhelfen macht aber auch Spaß (wenn auch nur den Machern).</p>
