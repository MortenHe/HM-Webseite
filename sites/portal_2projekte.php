                    <h1>Unberechenbarer Hitchcock</h1>
                    <p class='absatz'><span class='kursiv'>Unsere</span> ersten drei Hörspiele widmeten sich dem bekannten Detektivtrio aus Rocky Beach. In der Welt von Justus, Peter und Bob musste man nur etwas überzeichnen, das fing bereits bei Nebenfiguren an: so wurde <span class='kursiv'>Alfred Hitchcock</span> rasch zum unberechenbaren Kauz, der nicht allzu viel von dem jungen Detektivtrio hielt und nur als launischer Anrufer in Erscheinung trat. Und <span class='kursiv'>Onkel Titus</span> war ein windiger Geschäftsmann, der nebenbei illegal Müll entsorgte und beim Tod anderer Menschen schon mal auf ein Schnäppchen für sein Gebrauchtwarencenter spekulierte.</p>
                    <p>Bei den drei Hörspielen fanden nicht nur Personal und Handlungsmotive aus der Originalserie Verwendung: wir haben auch sehr gerne mit Original-Samples gearbeitet (besonders mit den Stimmen von Peter Pasetti und Andreas von der Meden). Samples eröffnen viele Möglichkeiten bei der Montage in die eigene Geschichte und im Zusammenspiel mit den eigenen Stimmen haben sie die gewünschte Wirkung entfaltet: endlich konnte man einmal selbst Hitchcock/Peter Pasetti am Telefon antworten ("Hallo?"-<span class='kursiv'>"Justus, wie geht's euch?"</span>-"Ah, Mr. Hitchcock...").</p>
                    <p>Selbst eingesprochen wurde bei uns stets der französische Gentlemanverbrecher <span class='kursiv'>Viktor Hugenay</span>, schon deshalb, weil ein nicht ganz überzeugender ausländischer Akzent oder Schwierigkeiten mit der Zweitsprache in einer guten Hörspielserie wünschenswert sind. <span class='kursiv'>Hugenay</span>, der berühmte, international gesuchte Kunstdieb ließ es sich jedenfalls nicht nehmen an allen drei Hörspielfolgen teilzunehmen, obwohl nicht immer ganz klar wurde, warum. Zum Dank durfte er dann jedes Mal auf elegante Weise und zum Erstaunen von Justus und Co. entkommen ("Er ist uns schon wieder entwischt!"), wie man es ja bereits von ihm gewohnt ist.</p>
