                    <h1>Piratenschatz oder Zirkus?</h1>
                    <p class='absatz'>Dass die drei Hörspiele unter unterschiedlichen Umständen und zu verschiedenen Zeiten entstanden, versteht sich eigentlich von selbst. Das erste beschäftigte uns in der 12. Klasse (bei einem Stufenstammtisch und flackernden Teelichtern kam der Anstoß dafür), als das dritte Hörspiel Gestalt annahm, war die Schulzeit vorbei. Und der Einsatz der Technik über die Hörspiele hinweg ist ohnehin ein Kapitel für sich. Das fängt mit den schlechten Keyboardeffekten an, die besonders in den ersten beiden Hörspielen zum Einsatz kamen.</p>
                    <p>Wenn wir je wieder ein ???-Hörspiel machen sollten, bleibt auf jeden Fall genügend Stoff für ein weiteres Abenteuer übrig. Manche klassischen Themen haben bisher zu wenig Beachtung gefunden, z.B. der Piratenschatz, Drogenhandel, der Täter als Brandstifter oder die Vergangenheit der Charaktere als Dreh- und Angelpunkt der Folge. Oder vielleicht wäre mal wieder eine klassische Rummel-/Zirkusfolge an der Reihe. Und ich warte auch immer noch darauf, dass die Originalserie endlich das Thema "Halloween" wieder aufgreift, sonst machen wir es. Außer Tante Mathilda würde wieder zur Arbeit rufen.</p>
