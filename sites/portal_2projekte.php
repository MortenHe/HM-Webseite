                    <h1>Mortons Geheimnis</h1>
                    <p class='absatz'>Da die Hörspiele immer in unserer Schule und Umgebung spielten (Lehrer waren die bevorzugten Bösewichte oder Zielscheiben in Gastauftritten), musste man zudem den Hauptschauplatz von Kalifornien nach Deutschland verlagern, eine passende Brücke war dank der Städtepartnerschaft von Kehl und Rocky Beach schnell geschlagen. Da war es schon schwieriger zu erklären, warum und wie <span class='kursiv'>Hauptkommissar Reynolds</span> seine Polizeigewalt ("Hände hoch - Sie sind verhaftet") auch in Deutschland ausüben sollte (dramaturgisch war dies aber äußerst wichtig). Das ???-Flugzeug aus den USA musste übrigens immer auf einem Modellflugplatz in der Nähe von Kehl landen. Wie Morton dort Justus, Peter und Bob pünktlich mit dem Rolls-Royce abholen konnte, blieb sein Geheimnis.</p>
                    <p>Mit den drei Hörspielen versuchte man natürlich außerdem typische Motive innerhalb der ???-Serie aufzugreifen. So durfte die in diesem Genre beliebte <span class='kursiv'>übernatürliche Erscheinung</span> (ob in Gestalt des Schulgeists oder Drachens) nicht fehlen. Am Ende des Falls entpuppte sie sich natürlich als fauler, von den Bösewichten fingierter Zauber, wie im dritten Hörspiel bereits im Titel (<span class='kursiv'>...und die Schmugglerbande</span>) angedeutet wurde. Und natürlich musste eine Hörspielfolge die <span class='kursiv'>ominöse Sekte</span> als Gegenspieler präsentieren, deren Mitglieder ihre zahlenmäßige Überlegenheit nicht gegen Justus und seine "Partner" ausspielen konnten.</p>
