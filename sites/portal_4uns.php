                    <h1>Wer sind wir?</h1>
                    <p class='absatz'>Wir sind zwei Hörspielmacher aus Kehl. Die Idee, selbst Hörspiele aufzunehmen, entstand noch zu Schulzeiten in der 12. Klasse bei einem Stufentreffen. Damals haben gleich einige unserer Freunde mitgemacht und bis in die Zeit nach dem Abitur entstanden mehrere ???-Fragezeichen-Hörspiele, über die ihr <a href='portal_2projekte.php?from=1'>hier</a> mehr erfahren könnt. Höhepunkt der Reihe war ohne Zweifel ein Live-Hörspiel im Kehler Jugendkeller. Ein Anruf und neue Aufträge von Alfred Hitchcock haben uns aber nie erreicht.</p>
                    <p>Im Jahr 2007 haben wir dann unser erstes großes Projekt verwirklicht: "Die Uhubande löst ihren ersten Fall". Für dieses Hörspiel haben wir uns viel Zeit genommen, da wir diesmal fast alles selbst gemacht haben: Manuskript, Charaktere, Musik, Cover und zum ersten Mal auch eine Homepage. Auch die Zahl der mitwirkenden Sprecher war mit 21 so groß wie nie. Über Hörspiel und Entstehung könnt ihr <a href='uhu_1start.php'>hier</a> einiges erfahren. Zu den schönen Erlebnissen, die wir mit der "Uhubande" hatten, gehören in jedem Fall die beiden von uns veranstalteten Hörspielnachmittage im Rahmen des Sommerferienprogramms der Stadt Rheinau.</p>
                    <p>Mitte 2008 begann dann die Arbeit an einem neuen Hörspiel. Nach den Detektivhörspielen haben wir nun ein Reiterhof-Hörspiel aufgenommen ("Reiterhof Dreikronen in Gefahr"), bei dem mehr als 25 Sprecher mitgewirkt haben. Alle wichtigen Infos dazu findet ihr <a href='3k_1start.php'>hier</a>. Bei diesem Hörspiel kam eine neue Aufnahmetechnik zum Zug. Bisher mussten immer alle Sprecher anwesend sein, die innerhalb einer Szene sprechen. Diesmal haben wir alle Sprecher einzeln aufgenommen und erst danach zusammengemischt.</p>
                    <p>Im Sommer 2009 haben wir unser erstes Kurzhörspiel mit dem Namen "Die Abschlussarbeit" aufgenommen. Aus technischer Sicht neu war diesmal, dass wir neben der Musik auch alle Geräusche selbst aufgenommen haben. <a href='abschlussarbeit_1start.php'>Hier</a> könnt ihr euch das fertige Stück anhören.</p>
                    <p>Mit "Die Trabanten" folgte 2011 unser 2. Kurzhörspiel. Ihr findet es <a href='trabanten_1start.php'>hier</a>.</p>
                    <p>Im Winter 2011 haben wir nach langer Vorbereitungszeit unser Science-Fiction-Hörspiel "Kris Planet" fertiggestellt. Neben vielen Soundeffekten und futuristischer Musik, kommt natürlich auch der Humor nicht zu kurz. Hört es euch <a href='kp_1start.php'>hier</a> an.</p>
                    <p>Unser 3. Kurzhörspiel erschien im Sommer 2012. Der Titel lautet "Der verfluchte Schatz". <a href='schatz_1start.php'>Hier</a> geht es zum Download.</p>
                    <p>Auch mit den kommenden Hörspielprojekten wollen wir neue Themen (Fantasy, Horror) ausprobieren und dabei versuchen, wie bei unseren bisherigen Hörspielen, klassische Hörspielserien zu variieren und humorvoll zu parodieren.</p>
