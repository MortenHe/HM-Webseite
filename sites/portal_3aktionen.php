                    <p class='absatz'>Am 8. Mai 2009 durften wir mit unserem Uhubande-Programm den Familiennachmittag der Rheinauer Kinderkulturtage gestalten. Neben einer kleinen Hörspielaufnahme zu unserem neuen Dreikronen-Hörspiel gab es auch einen Hindernis-Parcours für die Kinder. Dort mussten die Teilnehmer Bälle balancieren und dabei Slalom laufen. An einer weiteren Station galt es eine Geheimschrift mit Hilfe der passenden Übersetzungstabelle zu entschlüsseln. Zum Abschluss gab es für alle Brezeln und Getränke, bevor die Jugenddisko begann.</p>

